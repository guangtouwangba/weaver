'use client';

import React, { useState } from 'react';
import { Surface, Stack, Text, IconButton, Button, Chip, Modal } from '@/components/ui';
import { colors, radii, shadows } from '@/components/ui/tokens';
import { CloseIcon, FullscreenIcon, AutoAwesomeIcon, TrendingUpIcon, PeopleIcon, ArrowForwardIcon, ContentCopyIcon, ShareIcon, DockIcon } from '@/components/ui/icons';
import { KeyFinding } from '@/lib/api';

interface SummaryCardProps {
  title: string;
  summary: string;
  keyFindings: KeyFinding[];
  onClose: () => void;
  onDock?: () => void;
  onFullScreen?: () => void;
  onShare?: () => void;
  onCopy?: () => void;
}

export default function SummaryCard({
  title,
  summary,
  keyFindings,
  onClose,
  onDock,
  onShare,
  onCopy
}: SummaryCardProps) {
  const [isExpanded, setIsExpanded] = useState(false);

  const handleExpand = () => setIsExpanded(true);
  const handleCloseExpanded = () => setIsExpanded(false);

  return (
    <>
      {/* Compact Card */}
      <Surface
        elevation={0}
        radius="xl"
        bordered
        style={{
          width: 380,
          padding: 24,
          boxShadow: shadows.lg,
          position: 'relative',
        }}
      >
        {/* Header */}
        <Stack direction="row" align="start" style={{ marginBottom: 24 }}>
          {/* Icon */}
          <div
            style={{
              width: 40,
              height: 40,
              borderRadius: radii.lg,
              background: 'linear-gradient(135deg, #A78BFA 0%, #7C3AED 100%)', // Violet-400 to Violet-500
              display: 'flex',
              alignItems: 'center',
              justifyContent: 'center',
              color: 'white',
              marginRight: 16,
              boxShadow: '0 4px 12px rgba(124, 58, 237, 0.3)', // Violet shadow
              flexShrink: 0,
            }}
          >
            <AutoAwesomeIcon size="md" />
          </div>

          {/* Title Info */}
          <div style={{ flex: 1, minWidth: 0, overflow: 'hidden' }}>
            <Text
              variant="label"
              truncate
              style={{
                lineHeight: 1.2,
                marginBottom: 4,
                display: 'block',
                whiteSpace: 'nowrap',
                overflow: 'hidden',
                textOverflow: 'ellipsis',
              }}
            >
              {title}
            </Text>
            <Stack direction="row" align="center" gap={0} style={{ gap: 4 }}>
              <div
                style={{
                  width: 6,
                  height: 6,
                  borderRadius: '50%',
                  backgroundColor: colors.success[400],
                }}
              />
              <Text variant="caption" color="secondary">
                Generated by Weaver AI
              </Text>
            </Stack>
          </div>

          {/* Actions */}
          <Stack direction="row" gap={0} style={{ flexShrink: 0, gap: 4 }}>
            <IconButton size="sm" variant="ghost" onClick={handleExpand}>
              <FullscreenIcon size={16} />
            </IconButton>
            <IconButton size="sm" variant="ghost" onClick={onClose}>
              <CloseIcon size="sm" />
            </IconButton>
          </Stack>
        </Stack>

        {/* Tags */}
        <Stack direction="row" gap={1} style={{ marginBottom: 16 }}>
          <Chip
            label="STRATEGY"
            size="sm"
            color="primary"
            variant="soft"
          />
          <Chip
            label="INSIGHTS"
            size="sm"
            color="primary"
            variant="soft"
          />
        </Stack>

        {/* Content Preview */}
        <Text
          variant="bodySmall"
          color="secondary"
          style={{
            marginBottom: 24,
            lineHeight: 1.6,
            display: '-webkit-box',
            WebkitLineClamp: 3,
            WebkitBoxOrient: 'vertical',
            overflow: 'hidden'
          }}
        >
          {summary}
        </Text>

        {/* Highlights Box */}
        {keyFindings && keyFindings.length > 0 && (
          <div
            style={{
              padding: 16,
              backgroundColor: colors.background.subtle,
              borderRadius: radii.md,
              marginBottom: 24,
            }}
          >
            {keyFindings.slice(0, 2).map((finding, idx) => (
              <Stack key={idx} direction="row" align="start" gap={1} style={{ marginBottom: idx === 0 ? 12 : 0 }}>
                <div style={{ marginTop: 4 }}>
                  {idx === 0 ? <TrendingUpIcon size="sm" style={{ color: colors.success[500] }} /> : <PeopleIcon size={16} style={{ color: colors.primary[500] }} />}
                </div>
                <Text variant="bodySmall" style={{ fontSize: '0.85rem' }}>
                  <span style={{ fontWeight: 600 }}>{finding.label}:</span> {finding.content}
                </Text>
              </Stack>
            ))}
          </div>
        )}

        {/* Footer Button */}
        <Button
          variant="primary"
          onClick={handleExpand}
          style={{
            width: '100%',
            borderRadius: 50,
            textTransform: 'none',
            paddingTop: 8, paddingBottom: 8,
            boxShadow: '0 4px 12px rgba(124, 58, 237, 0.2)', // Violet shadow
          }}
          endIcon={<ArrowForwardIcon size="sm" />}
        >
          Read Full Summary
        </Button>
      </Surface>

      {/* Expanded Modal */}
      <Modal
        open={isExpanded}
        onClose={handleCloseExpanded}
        size="lg"
        style={{ padding: 0, overflow: 'hidden', display: 'flex', flexDirection: 'column' }}
      >
        {/* Modal Header */}
        <Stack
          direction="row"
          align="center"
          justify="between"
          style={{
            padding: 24,
            borderBottom: `1px solid ${colors.border.default}`,
            backgroundColor: 'rgba(255,255,255,0.8)',
            backdropFilter: 'blur(10px)'
          }}
        >
          <Stack direction="row" align="center" gap={2}>
            <div
              style={{
                width: 40,
                height: 40,
                borderRadius: radii.lg,
                background: 'linear-gradient(135deg, #A78BFA 0%, #7C3AED 100%)', // Violet-400 to Violet-500
                display: 'flex',
                alignItems: 'center',
                justifyContent: 'center',
                color: 'white',
              }}
            >
              <AutoAwesomeIcon size="md" />
            </div>
            <div>
              <Text variant="h6">{title}</Text>
              <Text variant="caption" color="secondary">
                AI Generated Summary
              </Text>
            </div>
          </Stack>
          <Stack direction="row" gap={1}>
            {onCopy && (
              <IconButton size="md" variant="ghost" onClick={onCopy} title="Copy to clipboard">
                <ContentCopyIcon size="md" />
              </IconButton>
            )}
            {onShare && (
              <IconButton size="md" variant="ghost" onClick={onShare} title="Share">
                <ShareIcon size="md" />
              </IconButton>
            )}
            {onDock && (
              <IconButton size="md" variant="ghost" onClick={onDock} title="Dock to board">
                <DockIcon size={20} />
              </IconButton>
            )}
            <IconButton size="md" variant="ghost" onClick={handleCloseExpanded} title="Close">
              <CloseIcon size="md" />
            </IconButton>
          </Stack>
        </Stack>

        {/* Modal Content */}
        <div style={{ padding: 32, overflowY: 'auto', flex: 1 }}>
          <Text variant="overline" color="primary" style={{ marginBottom: 8, display: 'block', fontWeight: 700 }}>
            EXECUTIVE SUMMARY
          </Text>
          <Text variant="body" color="secondary" style={{ lineHeight: 1.8, marginBottom: 32 }}>
            {summary}
          </Text>

          <Text variant="overline" color="primary" style={{ marginBottom: 16, display: 'block', fontWeight: 700 }}>
            KEY FINDINGS
          </Text>
          <div style={{ display: 'grid', gridTemplateColumns: '1fr 1fr', gap: 24 }}>
            {keyFindings && keyFindings.map((finding, idx) => (
              <Surface
                key={idx}
                elevation={0}
                radius="lg"
                bordered
                style={{ padding: 20, backgroundColor: colors.background.subtle }}
              >
                <Stack direction="row" align="center" gap={1} style={{ marginBottom: 4 }}>
                  <div
                    style={{
                      width: 8,
                      height: 8,
                      borderRadius: '50%',
                      backgroundColor: colors.primary[500],
                    }}
                  />
                  <Text variant="label">{finding.label}</Text>
                </Stack>
                <Text variant="bodySmall" color="secondary" style={{ lineHeight: 1.6 }}>
                  {finding.content}
                </Text>
              </Surface>
            ))}
          </div>
        </div>
      </Modal>
    </>
  );
}